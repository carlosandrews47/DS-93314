// Tabuada de multiplicação usando laço de repetição

console.log("=== TABUADA DO 7 ===");

const numero = 7;

for (let i = 1; i <= 10; i++) {
    let multiplicacao = numero * i;
    console.log(`${numero} x ${i} = ${multiplicacao}`);
}

console.log("--- Fim da tabuada ---");