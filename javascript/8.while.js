// 7. while.js
// Laço de repetição: While

console.log("=== Laço de Repetição WHILE ===");

// Exemplo 1: Contagem de 1 a 5
console.log("Contagem de 1 a 5:");
let i = 1;
while (i <= 5) {
    console.log(i);
    i++;
}

// Exemplo 2: Contagem regressiva
console.log("--- Contagem regressiva ---");
let j = 5;
while (j >= 1) {
    console.log(j);
    j--;
}

// Exemplo 3: Números pares
console.log("--- Números pares até 10 ---");
let k = 2;
while (k <= 10) {
    console.log(k);
    k += 2;
}

console.log("--- Fim dos laços while ---");