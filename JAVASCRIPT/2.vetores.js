// Criando um vetor.
const listaNomes = ['Marta', 'José', 'Maria']

console.log('\nExibindo todos os elementos: ')
console.log(listaNomes)

console.log('\nExibindo apenas o primeiro elemento: ')
console.log(listaNomes[0])

console.log('\nAdicionar a apenas um elemento no vetor: ')
listaNomes.push('Mariila')
console.log(listaNomes)

console.log('\nRemovendo o primeiro elemento no vetor: ')
listaNomes.shift()
console.log(listaNomes)

console.log('\nRemovendo o último elemento no vetor: ')
listaNomes.pop()

console.log(listaNomes)